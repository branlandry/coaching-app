export function getRecommendationsForBiomarker(biomarkerName: string, level: 'high' | 'low'): Recommendation[] {
  return recommendationsData.filter(rec => 
    rec.targetName === biomarkerName && 
    rec.level === level &&
    rec.targetType === 'measure'
  );
}

// Helper function to get all unique recommendation actions (for debugging)
export function getAllRecommendationActions(): string[] {
  const actions = new Set<string>();
  recommendationsData.forEach(rec => {
    actions.add(`${rec.action} ${rec.name}`);
  });
  return Array.from(actions).sort();
}

// Helper function to get all target biomarkers (for debugging)
export function getAllTargetBiomarkers(): string[] {
  const targets = new Set<string>();
  recommendationsData.forEach(rec => {
    targets.add(rec.targetName);
  });
  return Array.from(targets).sort();
}